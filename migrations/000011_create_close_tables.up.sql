-- Period Close & Reporting Schema
-- Handles period closing process, closing entries, and financial reporting

-- Create schema
CREATE SCHEMA IF NOT EXISTS close;

-- Enums
CREATE TYPE close.period_status AS ENUM ('open', 'soft_closed', 'hard_closed');
CREATE TYPE close.close_type AS ENUM ('period', 'quarter', 'year');
CREATE TYPE close.close_rule_type AS ENUM ('income_summary', 'retained_earnings', 'reclassification', 'accrual_reversal', 'currency_revaluation');
CREATE TYPE close.close_run_status AS ENUM ('pending', 'processing', 'completed', 'failed', 'reversed');
CREATE TYPE close.report_type AS ENUM ('trial_balance', 'income_statement', 'balance_sheet', 'cash_flow', 'changes_in_equity', 'general_ledger', 'subsidiary_ledger', 'aging', 'custom');
CREATE TYPE close.report_format AS ENUM ('summary', 'detailed', 'comparative', 'consolidated');
CREATE TYPE close.report_status AS ENUM ('pending', 'generating', 'completed', 'failed');

-- Period Close Status Table
-- Tracks the close status of each fiscal period
CREATE TABLE close.period_close_status (
    id CHAR(26) PRIMARY KEY,
    entity_id CHAR(26) NOT NULL REFERENCES entities(id),
    fiscal_period_id CHAR(26) NOT NULL,
    fiscal_year_id CHAR(26) NOT NULL,
    status close.period_status NOT NULL DEFAULT 'open',
    soft_closed_at TIMESTAMPTZ,
    soft_closed_by CHAR(26),
    hard_closed_at TIMESTAMPTZ,
    hard_closed_by CHAR(26),
    reopened_at TIMESTAMPTZ,
    reopened_by CHAR(26),
    reopen_reason TEXT,
    closing_journal_entry_id CHAR(26),
    notes TEXT,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    UNIQUE(entity_id, fiscal_period_id)
);

-- Close Rules Table
-- Defines rules for generating closing entries
CREATE TABLE close.close_rules (
    id CHAR(26) PRIMARY KEY,
    entity_id CHAR(26) NOT NULL REFERENCES entities(id),
    rule_code VARCHAR(20) NOT NULL,
    rule_name VARCHAR(100) NOT NULL,
    rule_type close.close_rule_type NOT NULL,
    close_type close.close_type NOT NULL,
    sequence_number INTEGER NOT NULL DEFAULT 1,
    source_account_type VARCHAR(20), -- revenue, expense, etc.
    source_account_id CHAR(26), -- specific account if applicable
    target_account_id CHAR(26) NOT NULL, -- income summary or retained earnings
    description TEXT,
    is_active BOOLEAN NOT NULL DEFAULT true,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    UNIQUE(entity_id, rule_code)
);

-- Close Runs Table
-- Tracks each closing process execution
CREATE TABLE close.close_runs (
    id CHAR(26) PRIMARY KEY,
    entity_id CHAR(26) NOT NULL REFERENCES entities(id),
    run_number VARCHAR(30) NOT NULL,
    close_type close.close_type NOT NULL,
    fiscal_period_id CHAR(26) NOT NULL,
    fiscal_year_id CHAR(26) NOT NULL,
    close_date DATE NOT NULL,
    status close.close_run_status NOT NULL DEFAULT 'pending',
    rules_executed INTEGER NOT NULL DEFAULT 0,
    entries_created INTEGER NOT NULL DEFAULT 0,
    total_debits DECIMAL(19,4) NOT NULL DEFAULT 0,
    total_credits DECIMAL(19,4) NOT NULL DEFAULT 0,
    currency_code CHAR(3) NOT NULL,
    closing_journal_entry_id CHAR(26),
    reversal_journal_entry_id CHAR(26),
    initiated_by CHAR(26) NOT NULL,
    completed_at TIMESTAMPTZ,
    reversed_at TIMESTAMPTZ,
    reversed_by CHAR(26),
    error_message TEXT,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    UNIQUE(entity_id, run_number)
);

-- Close Run Entries Table
-- Individual closing entries generated by a close run
CREATE TABLE close.close_run_entries (
    id CHAR(26) PRIMARY KEY,
    close_run_id CHAR(26) NOT NULL REFERENCES close.close_runs(id),
    close_rule_id CHAR(26) NOT NULL REFERENCES close.close_rules(id),
    sequence_number INTEGER NOT NULL,
    source_account_id CHAR(26) NOT NULL,
    source_account_code VARCHAR(20) NOT NULL,
    source_account_name VARCHAR(100) NOT NULL,
    target_account_id CHAR(26) NOT NULL,
    target_account_code VARCHAR(20) NOT NULL,
    target_account_name VARCHAR(100) NOT NULL,
    amount DECIMAL(19,4) NOT NULL,
    currency_code CHAR(3) NOT NULL,
    description TEXT,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

-- Report Templates Table
-- Defines report layouts and configurations
CREATE TABLE close.report_templates (
    id CHAR(26) PRIMARY KEY,
    entity_id CHAR(26) REFERENCES entities(id), -- NULL for system templates
    template_code VARCHAR(30) NOT NULL,
    template_name VARCHAR(100) NOT NULL,
    report_type close.report_type NOT NULL,
    report_format close.report_format NOT NULL DEFAULT 'summary',
    is_system_template BOOLEAN NOT NULL DEFAULT false,
    configuration JSONB NOT NULL DEFAULT '{}',
    -- Configuration can include:
    -- - account_ranges: [{from: "1000", to: "1999", label: "Current Assets"}]
    -- - groupings: ["account_type", "cost_center"]
    -- - columns: ["budget", "actual", "variance", "prior_year"]
    -- - filters: {account_types: [...], cost_centers: [...]}
    -- - subtotals: [{level: 1, label: "Total Assets"}]
    is_active BOOLEAN NOT NULL DEFAULT true,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

-- Report Runs Table
-- Tracks generated reports
CREATE TABLE close.report_runs (
    id CHAR(26) PRIMARY KEY,
    entity_id CHAR(26) NOT NULL REFERENCES entities(id),
    report_number VARCHAR(30) NOT NULL,
    template_id CHAR(26) REFERENCES close.report_templates(id),
    report_type close.report_type NOT NULL,
    report_format close.report_format NOT NULL,
    report_name VARCHAR(200) NOT NULL,
    fiscal_period_id CHAR(26),
    fiscal_year_id CHAR(26),
    as_of_date DATE NOT NULL,
    comparison_period_id CHAR(26), -- for comparative reports
    comparison_year_id CHAR(26),
    parameters JSONB NOT NULL DEFAULT '{}',
    status close.report_status NOT NULL DEFAULT 'pending',
    generated_by CHAR(26) NOT NULL,
    generated_at TIMESTAMPTZ,
    completed_at TIMESTAMPTZ,
    error_message TEXT,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

-- Report Data Table
-- Stores the actual report data rows
CREATE TABLE close.report_data (
    id CHAR(26) PRIMARY KEY,
    report_run_id CHAR(26) NOT NULL REFERENCES close.report_runs(id) ON DELETE CASCADE,
    row_number INTEGER NOT NULL,
    row_type VARCHAR(20) NOT NULL, -- header, detail, subtotal, total, blank
    indent_level INTEGER NOT NULL DEFAULT 0,
    account_id CHAR(26),
    account_code VARCHAR(20),
    account_name VARCHAR(100),
    description VARCHAR(200),
    cost_center_id CHAR(26),
    cost_center_code VARCHAR(20),
    amount_1 DECIMAL(19,4), -- current period/YTD
    amount_2 DECIMAL(19,4), -- budget or prior period
    amount_3 DECIMAL(19,4), -- variance or prior year
    amount_4 DECIMAL(19,4), -- variance percent or other
    amount_5 DECIMAL(19,4), -- additional column
    currency_code CHAR(3),
    is_bold BOOLEAN NOT NULL DEFAULT false,
    is_underlined BOOLEAN NOT NULL DEFAULT false,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

-- Scheduled Reports Table
-- For recurring report generation
CREATE TABLE close.scheduled_reports (
    id CHAR(26) PRIMARY KEY,
    entity_id CHAR(26) NOT NULL REFERENCES entities(id),
    template_id CHAR(26) NOT NULL REFERENCES close.report_templates(id),
    schedule_name VARCHAR(100) NOT NULL,
    frequency VARCHAR(20) NOT NULL, -- daily, weekly, monthly, quarterly, yearly
    day_of_week INTEGER, -- 0-6 for weekly
    day_of_month INTEGER, -- 1-31 for monthly
    parameters JSONB NOT NULL DEFAULT '{}',
    recipients JSONB NOT NULL DEFAULT '[]', -- email addresses or user IDs
    output_format VARCHAR(20) NOT NULL DEFAULT 'pdf', -- pdf, excel, csv
    is_active BOOLEAN NOT NULL DEFAULT true,
    last_run_at TIMESTAMPTZ,
    next_run_at TIMESTAMPTZ,
    created_by CHAR(26) NOT NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

-- Year End Checklist Table
