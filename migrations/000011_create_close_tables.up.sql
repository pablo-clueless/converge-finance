-- Run Number Generation Function
CREATE OR REPLACE FUNCTION close.generate_close_run_number(p_entity_id CHAR(26), p_prefix VARCHAR(10))
RETURNS VARCHAR(30) AS $$
DECLARE
    v_year TEXT;
    v_sequence INTEGER;
    v_run_number VARCHAR(30);
BEGIN
    v_year := TO_CHAR(CURRENT_DATE, 'YYYY');

    SELECT COALESCE(MAX(
        CAST(SUBSTRING(run_number FROM LENGTH(p_prefix) + 6 FOR 6) AS INTEGER)
    ), 0) + 1
    INTO v_sequence
    FROM close.close_runs
    WHERE entity_id = p_entity_id
    AND run_number LIKE p_prefix || v_year || '%';

    v_run_number := p_prefix || v_year || '-' || LPAD(v_sequence::TEXT, 6, '0');

    RETURN v_run_number;
END;
$$ LANGUAGE plpgsql;

